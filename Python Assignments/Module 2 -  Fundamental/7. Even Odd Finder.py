'''Qs 7 :- Write a Python program to find whether a given number is even or odd, print
out an appropriate message to the user
'''

print("\n\n *** Checker Even Number or Odd Number ***\n")

num = int(input("Enter a Number : "))

if num % 2 == 0 :
    print(num ," is Even Number")
else :
    print(num ,"is ODD Number")


