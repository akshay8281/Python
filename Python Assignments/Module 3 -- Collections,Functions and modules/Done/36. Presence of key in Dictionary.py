# 36. How Do You Check The Presence Of A Key In A Dictionary?.


data = {"kishan": 123, "akshay": 888, "bhavin": 222,"jay":777}

keyChecker = input("Enter a Key to Check : ")

if keyChecker in data:
    
    print(keyChecker," : key exists in the dictionary.")
else:
    print(keyChecker," : key does not exist in the dictionary.")











