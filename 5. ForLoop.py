print("For Loop Concept in Python : ")

for i in range (11):
    print(i)
print("*************")

for i in range (0,11):
    print(i)
print("*************")

for i in range (0,11,2):
    print(i)
print("*************")

for i in range (11,0,-2):
    print(i)
print("*************")#For Comments in Python
