# Lambda

str1 = "Welcome To The World"

upper = lambda string:string.upper()
print("\nUppercase String : ",upper(str1))


# Using Lambda Function
cube = lambda x:x*x*x
print("\nCube Using Lambda: ",cube(10))

# using Function
def cube1(x):
    return x*x*x
print("Cube Using Function : ",cube1(10))
